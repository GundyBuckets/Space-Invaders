# Obstacle class represents a collection of blocks forming an obstacle
class Obstacle:
    def __init__(self, x, y):
        # Create a group to hold all the blocks
        self.blocks_group = pygame.sprite.Group()
        # Iterate through the grid to create blocks
        for row in range(len(grid)):
            for column in range(len(grid[0])):
                if grid[row][column] == 1:
                    # Create a block at the specified position
                    block = Block(x + column * 3, y + row * 3)
                    # Add the block to the group
                    self.blocks_group.add(block)
